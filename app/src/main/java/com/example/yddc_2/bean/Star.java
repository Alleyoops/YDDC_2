package com.example.yddc_2.bean;

public class Star {
    int gre;
    int red;
    public Star(int gre,int red) {
        this.gre = gre;
        this.red = red;
    }

    public int getGre() {
        return gre;
    }

    public int getRed() {
        return red;
    }

    public void setGre(int gre) {
        this.gre = gre;
    }

    public void setRed(int red) {
        this.red = red;
    }
}
